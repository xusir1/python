name_list = ["徐磊", "曹雨", "方春娇", "沈晓燕"]
number_list = [2,6,3,7,1,5]
print(name_list)
print(number_list)

#升序排列
name_list.sort()
number_list.sort()
print(name_list)
print(number_list)

#逆序排列
name_list.sort(reverse=True)
number_list.sort(reverse=True)
print(name_list)
print(number_list)

#反转排列
name_list.reverse()
number_list.reverse()
print(name_list)
print(number_list)